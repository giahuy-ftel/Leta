    p_timer_reload_watchdog = akos_timer_create(TIMER_RELOAD_WATCH_DOG, 0, reload_watchdog, 0, 500, TIMER_PERIODIC); /* Update every 500ms*/
    akos_timer_start(p_timer_update_watch_time, 0);
