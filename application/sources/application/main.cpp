void task_gui(void *p_arg)
{   
    while (1)
    {
        if (!app_usb_video_active())
        {
            gui_run();
        }
        else
        {
            akos_thread_delay(1);
        }
    }
}

void task_page_mng(void *p_arg)
{
    msg_t * msg;
    gui_page_init();  
    while (1)
    {
        msg = akos_thread_wait_for_msg(0);
        gui_page_run(msg);
        if(msg != NULL) akos_message_free(msg);
    }
}

RTCSTM32 watch_time;
RTCDateTime watch_time_now;
watch_config config;
MPU6050_t mpu;
float bat_voltage;
float watch_temp;

ak_timer_t *p_timer_update_watch_time;
ak_timer_t *p_timer_reload_watchdog;
ak_timer_t *p_timer_countdown_screen_time;

static const float BATTERY_SHUTDOWN_VOLTAGE = 3.35f;
static const uint8_t BATTERY_LOW_SAMPLE_COUNT = 6;
static uint8_t battery_low_count;

static void update_watch_time(void)
{
    watch_time_now = watch_time.getDateTime();
}

static void reload_watchdog(void)
{
    watchdog_hw_reload();
    watch_temp = adc_get_temp();
    bat_voltage = adc_get_bat_voltage();
    if(bat_voltage < BATTERY_SHUTDOWN_VOLTAGE) {
        if(battery_low_count < BATTERY_LOW_SAMPLE_COUNT) {
            battery_low_count++;
        }
        if(battery_low_count >= BATTERY_LOW_SAMPLE_COUNT) {
            power_pin_off();
        }
    }
    else {
        battery_low_count = 0;
    }
}

void countdown_screen_time(void)
{
    p_timer_countdown_screen_time = NULL;
    config_data_write(&config);
    power_pin_off();
}

void screen_timeout_restart(uint32_t timeout_ms)
{
    if(p_timer_countdown_screen_time != NULL)
    {
        akos_timer_remove(p_timer_countdown_screen_time);
    }

    p_timer_countdown_screen_time = akos_timer_create(TIMER_COUNT_DOWN_SCREEN_TIME, 0, countdown_screen_time, 0, 0, TIMER_ONE_SHOT);
    if(p_timer_countdown_screen_time != NULL)
    {
        akos_timer_start(p_timer_countdown_screen_time, timeout_ms);
    }
}

void screen_timeout_restart_configured(void)
{
    screen_timeout_restart(config.screen_time * 1000);
}






