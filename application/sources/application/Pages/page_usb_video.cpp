static uint32_t frame_count;
static uint32_t keepalive_tick;
static bool last_connected;

#define USB_VIDEO_SCREEN_TIMEOUT_MS 120000U
#define USB_VIDEO_KEEPALIVE_MS      10000U

static void draw_status(const char *state)
{
	gui_clear_buff();
	gui_window_draw_str(&window_screen_size, 18, 8, "USB Video", DRAW_STATE_ON);
	gui_window_draw_str(&window_screen_size, 12, 28, state, DRAW_STATE_ON);
	gui_refresh();
}

static void setup(void *p_arg)
{
	(void)p_arg;

	frame_count = 0;
	last_connected = false;
	keepalive_tick = akos_thread_get_tick();
	watchdog_hw_reload();
	screen_timeout_restart(USB_VIDEO_SCREEN_TIMEOUT_MS);
	app_usb_video_set_frame_buffer(gui_scr_buffer);
	app_usb_video_set_active(true);
	app_usb_video_init();
	draw_status("waiting host");
}

static void loop(void *p_arg)
{
	(void)p_arg;

	watchdog_hw_reload();
	app_usb_video_task();

	bool connected = app_usb_video_connected();
	if (connected != last_connected && frame_count == 0) {
		last_connected = connected;
		draw_status(connected ? "waiting stream" : "connect USB");
	}

	if (app_usb_video_frame_ready()) {
		oled_update(app_usb_video_frame_buffer());
		frame_count++;
	}

	if ((akos_thread_get_tick() - keepalive_tick) >= USB_VIDEO_KEEPALIVE_MS) {
		keepalive_tick = akos_thread_get_tick();
		screen_timeout_restart(USB_VIDEO_SCREEN_TIMEOUT_MS);
	}

	akos_thread_delay(1);
}

static void event_hdler(void *p_event)
{
	msg_t *p_msg = (msg_t *)p_event;

	switch (p_msg->sig)
	{
	case SIG_BTN_DR_PRESSED:
		app_usb_video_set_active(false);
		screen_timeout_restart_configured();
		page_jump_to(PAGE_WATCH_ID);
		break;

	default:
		break;
	}
}

void page_usb_video_reg(void)
{
	page_create(PAGE_USB_VIDEO_ID, "USB Video", ICON, setup, loop, event_hdler);
}
