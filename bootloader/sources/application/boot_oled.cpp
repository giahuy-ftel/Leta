static Adafruit_oled_drv bootloader_oled;
static bool bootloader_oled_ready;

extern "C" void __cxa_pure_virtual(void)
{
	while (1) {
	}
}

static void bootloader_oled_draw_dfu_screen(const char *text, bool show_bar, uint8_t percent)
{
	const uint8_t bar_x = (OLED_WIDTH - DFU_BAR_WIDTH) / 2;
	uint8_t fill_width = 0;
	uint8_t text_x = 0;

	if (percent > 100) {
		percent = 100;
	}

	if (text != 0) {
		uint8_t text_width = 0;
		while (text[text_width] != '\0') {
			text_width++;
		}
		text_width = (uint8_t)(text_width * 6);
		text_x = (OLED_WIDTH - text_width) / 2;
	}

	if (!bootloader_oled_ready) {
		bootloader_oled_ready = bootloader_oled.initialize();
	}

	if (show_bar) {
		fill_width = (uint8_t)(((DFU_BAR_WIDTH - 2) * percent) / 100);
	}

	bootloader_oled.clear(false);
	bootloader_oled.drawBitmap((OLED_WIDTH - USB_ICON_WIDTH) / 2, DFU_ICON_Y,
			usb_icon, USB_ICON_WIDTH, USB_ICON_HEIGHT, WHITE);
	if (text != 0) {
		bootloader_oled.setTextColor(WHITE);
		bootloader_oled.setCursor(text_x, DFU_TEXT_Y);
		bootloader_oled.print(text);
	}
	if (show_bar) {
		bootloader_oled.drawRect(bar_x, DFU_BAR_Y, DFU_BAR_WIDTH, DFU_BAR_HEIGHT, WHITE);
		if (fill_width > 0) {
			bootloader_oled.fillRect(bar_x + 1, DFU_BAR_Y + 1,
					fill_width, DFU_BAR_HEIGHT - 2, WHITE);
		}
	}
	bootloader_oled.update();
	bootloader_oled.display_on();
}

extern "C" void bootloader_oled_show_dfu_mode(void)
{
	bootloader_oled_draw_dfu_screen("DFU mode", false, 0);
}

extern "C" void bootloader_oled_show_usb_icon(void)
{
	bootloader_oled_draw_dfu_screen(0, false, 0);
}

extern "C" void bootloader_oled_show_dfu_progress(uint8_t percent)
{
	bootloader_oled_draw_dfu_screen("Loading", true, percent);
}

extern "C" void bootloader_oled_show_dfu_done(void)
{
	bootloader_oled_draw_dfu_screen("Done", true, 100);
}

extern "C" void bootloader_oled_display_off(void)
{
	if (bootloader_oled_ready) {
		bootloader_oled.display_off();
	}
}
